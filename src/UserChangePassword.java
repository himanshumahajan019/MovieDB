    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String opassword = jPasswordField1.getText();
        String npassword = jPasswordField2.getText();
        String conpassword = jPasswordField3.getText();
        boolean flag = npassword.equals(conpassword);
        if(flag != true){
            JOptionPane.showMessageDialog(this, "New Password & Comfirm Password Must Match.");
        }else{
            String ans = MyClient.userChangePassword(opassword, npassword);
            
            if(ans.equals("success")){
                JOptionPane.showMessageDialog(this, "Password Changed Successfully. Now Login With New Password");
                UserLogin obj = new UserLogin();
                obj.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, ans);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

